class VariavelFloat{
	public static void main (String arg[]){
			// declaração variavel inteira
			float x = 10; // atribuição de valor de "x"
			float y = 22; // atribuição de valor de "y"
			// imprime no terminal
			System.out.print ("Soma\t" + (x+y) );
			System.out.print ("\nDivisao\t" + (y/x) );
			System.out.print ("\nMultiplicacao\t" + (x*y) );
			System.out.print ("\nSubtracao\t" + (y-x) );
	}
}