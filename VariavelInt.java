class VariavelInt{
	public static void main (String arg[]){
			// declaração variavel inteira
			int x = 10; // atribuição de valor
			int y = 2;
			// imprime no terminal
			System.out.print ("Soma\t" + (x+y) );
			System.out.print ("\nDivisao\t" + (x/y) );
			System.out.print ("\nMultiplicacao\t" + (x*y) );
			System.out.print ("\nSubtracao\t" + (x-y) );
	}
}